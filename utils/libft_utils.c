#include "../cub3D.h"

/* ############### Count Char ############### */
int	ft_count(char **map)
{
	int	x;

	x = 0;
	while (map[0][x])
		x++;
	return (x);
}

/* ############# Count Line ###################### */
int	f_strlen(char **map)
{
	int	y;

	y = 0;
	while (map[y])
		y++;
	return (y);
}
